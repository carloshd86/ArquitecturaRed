#include "gameapplication.h"
#include "globals.h"
#include "Net\Net.h"


GameApplication * GameApplication::m_pInstance = nullptr;

// **************************************************************************************
//
// **************************************************************************************

GameApplication::GameApplication() {}

// **************************************************************************************
//
// **************************************************************************************

GameApplication::GameApplication() {}

// **************************************************************************************
//
// **************************************************************************************

GameApplication * GameApplication::Instance() {
	if (!m_pInstance)
		m_pInstance = new GameApplication();

	return m_pInstance;
}

// **************************************************************************************
//
// **************************************************************************************

INet * GameApplication::GetNet() {
	if (!m_pNet)
		m_pNet = new CNet();

	return m_pNet;
}

